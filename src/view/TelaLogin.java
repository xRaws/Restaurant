    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed

        FuncionarioBuild funcionarioBuild = new FuncionarioBuild();
            
        String user = txtNome.getText();
        String senha = txtSenha.getText();
        
        
        funcionarioBuild.where("user","'"+user+"'");
        funcionarioBuild.where("senha","'"+ senha+"'");
        List<Funcionario> funcionarios = funcionarioBuild.get();
         
        if(funcionarios.size() > 0) {
            Principal tela = new Principal();
            tela.setLogado(user);
            this.setVisible(false);
            tela.setVisible(true);
        } else {
            // Cria o JOptionPane por showMessageDialog
            JOptionPane.showMessageDialog(null,"Usuario ou Senha inv√°lido!!", //mensagem
                "Erro ao Logar", // titulo da janela 
                JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btEntrarActionPerformed

